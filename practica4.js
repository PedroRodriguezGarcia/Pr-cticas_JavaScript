// Declaración de variables
let numero = 5;
let texto = "5";

// Comparación usando ==
console.log(numero == texto);  // true
// == compara valores
// Aquí "5" (string) se compara 5 (number) por eso da true

// Comparación usando ===
console.log(numero === texto); // false
// === compara tipos de los valores
// numero es Number y texto es String, son tipos distintos, por eso da false
